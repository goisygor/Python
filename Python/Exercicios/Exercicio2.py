# Declarando as Strings e os nomes 
Nome = "Ygor"
Sobrenome = "Gois"
    
# concatenando as Strings para formar o nome completo

nome_completo =  Nome + " " + Sobrenome
print("Nome Completo: ",nome_completo)

#Declarando uma frase

Frase = "As vezes Sabedoria é observar e calar, mesmo estando cheio de argumentos"

#Convertendo todas letras para Maiúsculas
frase_maiuscula = Frase.upper()
print("Frase em maiúsculas: ", frase_maiuscula )

#Convertendo todas as letras em Minúsculas
frase_minuscula = Frase.lower()
print("Frase em minúscula", frase_minuscula)

#Substituindo uma palavra por outra na frase 
frase_substituida = Frase.replace("Sabedoria" "Burrice")
print("Frase com Substituição", frase_substituida)